-- Индекс для ускорения поиска физических экземпляров по статусу
CREATE NONCLUSTERED INDEX Index_ФизическийЭкземпляр_Статус
ON Физический_Экземпляр(статус)
INCLUDE (id_Представление, id_Отдел, id_Экземпляр)
ON INDEXES;

-- Индекс для ускорения поиска ресурсов по авторам
CREATE NONCLUSTERED INDEX Index_АвторКнига_idРесурс
ON Автор_Книга(id_Ресурс)
INCLUDE (id_Автор)
ON INDEXES;

-- Индекс для ускорения соединения физических экземпляров и представлений ресурсов
CREATE NONCLUSTERED INDEX Index_ФизическийЭкземпляр_idПредставление
ON Физический_Экземпляр(id_Представление)
INCLUDE (статус, id_Отдел)
ON INDEXES;

-- Индекс для ускорения поиска авторов статей
CREATE NONCLUSTERED INDEX Index_АвторСтатья_idСтатья
ON Автор_Статья(id_Статья)
INCLUDE (id_Автор)
ON INDEXES;

-- Индекс для ускорения выборки операций движения фонда
CREATE NONCLUSTERED INDEX Index_ДвижениеФонда_ЭкземплярДата
ON Движение_Фонда(id_Экземпляр, дата_операции DESC)
INCLUDE (тип_операции, примечание)
ON INDEXES;


