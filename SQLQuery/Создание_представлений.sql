---------------------------------------------------
-- 1. vw_ПоискВКаталоге
-- Вывод всех книг с авторами, жанрами, издательствами, форматом/типом и статусом
---------------------------------------------------
CREATE OR ALTER VIEW vw_ПоискВКаталоге
AS
SELECT
    К.id_Ресурс,
    К.название AS Книга,

    -- Агрегируем всех авторов одной книги
    (SELECT STRING_AGG(A2.фамилия + N' ' + A2.имя + ISNULL(N' ' + A2.отчество, N''), N', ')
     FROM Автор_Книга АК2
     INNER JOIN Автор A2 ON АК2.id_Автор = A2.id_Автор
     WHERE АК2.id_Ресурс = К.id_Ресурс
    ) AS Авторы,

    Ж.название AS Жанр,
    И.название AS Издательство,
    ФХ.название AS Формат,
    ТХ.название AS ТипХранения,
    ФЭ.статус AS СтатусФизическогоЭкземпляра,
    О.название AS Отдел

FROM Книга К
INNER JOIN Жанр Ж ON К.id_Жанр = Ж.id_Жанр
INNER JOIN Издательство И ON К.id_Издательство = И.id_Издательство
INNER JOIN Представление_Ресурса ПР ON К.id_Ресурс = ПР.id_Ресурс
INNER JOIN Формат_Хранения ФХ ON ПР.id_Формат = ФХ.id_Формат
INNER JOIN Тип_Хранения ТХ ON ПР.id_Тип_Хранения = ТХ.id_Тип_Хранения
LEFT JOIN Физический_Экземпляр ФЭ ON ПР.id_Представление = ФЭ.id_Представление
LEFT JOIN Отдел О ON ФЭ.id_Отдел = О.id_Отдел;
GO

---------------------------------------------------
-- 2. vw_ПоискПоКлассификатору
-- Поиск книг по жанрам и поджанрам
---------------------------------------------------
CREATE OR ALTER VIEW vw_ПоискПоКлассификатору
AS
SELECT
    К.id_Ресурс,
    К.название AS Книга,

    Ж.название AS Жанр,
    ISNULL(Ж2.название, N'') AS РодительскийЖанр,

    -- Агрегируем всех авторов книги
    (SELECT STRING_AGG(A2.фамилия + N' ' + A2.имя + ISNULL(N' ' + A2.отчество, N''), N', ')
     FROM Автор_Книга АК2
     INNER JOIN Автор A2 ON АК2.id_Автор = A2.id_Автор
     WHERE АК2.id_Ресурс = К.id_Ресурс
    ) AS Авторы

FROM Книга К
INNER JOIN Жанр Ж ON К.id_Жанр = Ж.id_Жанр
LEFT JOIN Жанр Ж2 ON Ж.id_Родительский_жанр = Ж2.id_Жанр;
GO

---------------------------------------------------
-- 3. vw_ДоступныеЭкземпляры
-- Только физические экземпляры со статусом 'доступен'
---------------------------------------------------
CREATE OR ALTER VIEW vw_ДоступныеЭкземпляры
AS
SELECT
    ФЭ.id_Экземпляр,
    ФЭ.инвентарный_номер,
    К.название AS Книга,

    -- Агрегируем авторов книги
    (SELECT STRING_AGG(A2.фамилия + N' ' + A2.имя + ISNULL(N' ' + A2.отчество, N''), N', ')
     FROM Автор_Книга АК2
     INNER JOIN Автор A2 ON АК2.id_Автор = A2.id_Автор
     WHERE АК2.id_Ресурс = К.id_Ресурс
    ) AS Авторы,

    ФХ.название AS Формат,
    ТХ.название AS ТипХранения,
    О.название AS Отдел

FROM Физический_Экземпляр ФЭ
INNER JOIN Представление_Ресурса ПР ON ФЭ.id_Представление = ПР.id_Представление
INNER JOIN Книга К ON ПР.id_Ресурс = К.id_Ресурс
INNER JOIN Формат_Хранения ФХ ON ПР.id_Формат = ФХ.id_Формат
INNER JOIN Тип_Хранения ТХ ON ПР.id_Тип_Хранения = ТХ.id_Тип_Хранения
INNER JOIN Отдел О ON ФЭ.id_Отдел = О.id_Отдел
WHERE ФЭ.статус = N'доступен';
GO

---------------------------------------------------
-- 4. vw_СостояниеФонда
-- Текущее состояние физических и электронных ресурсов с последней операцией
---------------------------------------------------
CREATE OR ALTER VIEW vw_СостояниеФонда
AS
SELECT
    К.id_Ресурс,
    К.название AS Книга,

    -- Агрегируем всех авторов книги
    (SELECT STRING_AGG(A2.фамилия + N' ' + A2.имя + ISNULL(N' ' + A2.отчество, N''), N', ')
     FROM Автор_Книга АК2
     INNER JOIN Автор A2 ON АК2.id_Автор = A2.id_Автор
     WHERE АК2.id_Ресурс = К.id_Ресурс
    ) AS Авторы,

    ПР.id_Представление,
    ФХ.название AS Формат,
    ТХ.название AS ТипХранения,
    ФЭ.статус AS СтатусФизическогоЭкземпляра,
    О.название AS Отдел,

    -- Последняя операция физического экземпляра
    CASE 
        WHEN ФЭ.id_Экземпляр IS NOT NULL THEN ЛП.тип_операции
        ELSE NULL
    END AS ПоследняяОперация,

    -- Дата выдачи, если экземпляр выдан
    CASE 
        WHEN ФЭ.статус = N'выдан' THEN ЛП.дата_операции
        ELSE NULL
    END AS ДатаВыдачи,

    ЛП.примечание AS ПримечаниеПоследнейОперации,

    -- Статус и размер электронного ресурса
    ЭР.статус AS СтатусЭлектронногоРесурса,
    ЭР.размер_файла AS РазмерЭлектронногоРесурса

FROM Книга К
INNER JOIN Представление_Ресурса ПР ON К.id_Ресурс = ПР.id_Ресурс
INNER JOIN Формат_Хранения ФХ ON ПР.id_Формат = ФХ.id_Формат
INNER JOIN Тип_Хранения ТХ ON ПР.id_Тип_Хранения = ТХ.id_Тип_Хранения
LEFT JOIN Физический_Экземпляр ФЭ ON ПР.id_Представление = ФЭ.id_Представление
LEFT JOIN Отдел О ON ФЭ.id_Отдел = О.id_Отдел

-- Последняя операция физического экземпляра
OUTER APPLY
(
    SELECT TOP 1 *
    FROM Движение_Фонда ДФ
    WHERE ДФ.id_Экземпляр = ФЭ.id_Экземпляр
    ORDER BY ДФ.дата_операции DESC, ДФ.id_Движение DESC
) ЛП

-- Электронные ресурсы
LEFT JOIN Электронный_Ресурс ЭР ON ПР.id_Представление = ЭР.id_Представление;
GO
